i = int(input("Введите годовую процентную ставку: "))
s = float(input("Введите сумму займа: "))
n = int(input("Введите срок займа в месяцах: "))
p = i/12/100
m = (s*p * (1+p)**n) / ((1+p)**n - 1)
print("Размер ежемесячной выплаты составит: ", round(m, 2))
s_res = round(m, 2)*n
print("Всего выплата банку составит: ", round(s_res, 2))
over_s = s_res-s
print("Переплата по кредиту составит: ", round(over_s, 2))